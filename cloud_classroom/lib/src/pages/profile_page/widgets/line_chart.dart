import 'package:fl_chart/fl_chart.dart';
import 'package:flutter/material.dart';

const chartLabelsTextStyle = TextStyle(
  color: Colors.grey,
  fontSize: 14.0,
  fontWeight: FontWeight.w500,
);

class LineChartSubmit extends StatefulWidget {
  @override
  State<StatefulWidget> createState() => LineChartSubmitState();
}

class LineChartSubmitState extends State<LineChartSubmit> {
  bool isShowingMainData;

  @override
  void initState() {
    super.initState();
    isShowingMainData = true;
  }

  @override
  Widget build(BuildContext context) {
    return AspectRatio(
      aspectRatio: 1,
      child: Container(
        decoration: const BoxDecoration(
          borderRadius: BorderRadius.vertical(
            top: Radius.circular(
              30.0,
            ),
          ),
          color: Colors.white,
        ),
        child: Stack(
          children: <Widget>[
            Column(
              crossAxisAlignment: CrossAxisAlignment.stretch,
              children: <Widget>[
                const SizedBox(
                  height: 24,
                ),
                const Text(
                  'Deadlines statistic in this Semester',
                  style: TextStyle(
                    color: Color(0xff827daa),
                    fontSize: 22.0,
                    fontWeight: FontWeight.bold,
                  ),
                  textAlign: TextAlign.center,
                ),
                const SizedBox(
                  height: 36.0,
                ),
                Expanded(
                  child: Padding(
                    padding: const EdgeInsets.symmetric(
                      horizontal: 4.0,
                    ),
                    child: LineChart(
                      isShowingMainData ? sampleData1() : sampleData2(),
                      swapAnimationDuration: const Duration(milliseconds: 250),
                    ),
                  ),
                ),
                const SizedBox(
                  height: 20.0,
                ),
              ],
            ),
          ],
        ),
      ),
    );
  }

  LineChartData sampleData1() {
    return LineChartData(
      lineTouchData: LineTouchData(
        touchTooltipData: LineTouchTooltipData(
          tooltipBgColor: Colors.blueGrey.withOpacity(0.7),
        ),
        touchCallback: (LineTouchResponse touchResponse) {},
        handleBuiltInTouches: true,
      ),
      gridData: FlGridData(
        show: false,
      ),
      titlesData: FlTitlesData(
        bottomTitles: SideTitles(
          margin: 10.0,
          showTitles: true,
          textStyle: TextStyle(
            color: Colors.grey,
            fontSize: 12.5,
            fontWeight: FontWeight.w600,
          ),
          rotateAngle: 35.0,
          getTitles: (value) {
            switch (value.toInt()) {
              case 1:
                return 'Sep';
              case 2:
                return 'Oct';
              case 3:
                return 'Nov';
              case 4:
                return 'Dec';
              case 5:
                return 'Jan';
              default:
                return '';
            }
          },
        ),
        leftTitles: SideTitles(
          margin: 10.0,
          showTitles: true,
          textStyle: chartLabelsTextStyle,
          getTitles: (value) {
            if (value == 0) {
              return '0';
            } else if (value % 3 == 0) {
              return '${value ~/ 3 * 10}';
            }
            return '';
          },
        ),
      ),
      borderData: FlBorderData(
        show: false,
        border: const Border(
          bottom: BorderSide(
            color: Color(0xff4e4965),
            width: 0,
          ),
          left: BorderSide(
            color: Colors.transparent,
          ),
          right: BorderSide(
            color: Colors.transparent,
          ),
          top: BorderSide(
            color: Colors.transparent,
          ),
        ),
      ),
      minX: 0,
      maxX: 6,
      maxY: 15,
      minY: 0,
      lineBarsData: linesBarData1(),
    );
  }

  List<LineChartBarData> linesBarData1() {
    final LineChartBarData lineChartBarData1 = LineChartBarData(
      spots: [
        FlSpot(1, 0),
        FlSpot(2, 10.5),
        FlSpot(3, 9.4),
        FlSpot(4, 12.4),
        FlSpot(5, 4.8),
      ],
      isCurved: true,
      colors: [
        const Color(0xFFFF4C4C),
      ],
      barWidth: 2.5,
      isStrokeCapRound: true,
      dotData: FlDotData(
        show: false,
      ),
      belowBarData: BarAreaData(
        show: false,
      ),
    );
    final LineChartBarData lineChartBarData2 = LineChartBarData(
      spots: [
        FlSpot(1, 0),
        FlSpot(2, 7.8),
        FlSpot(3, 9.2),
        FlSpot(4, 11.8),
        FlSpot(5, 8.6),
      ],
      isCurved: true,
      colors: [
        Colors.blueAccent,
      ],
      barWidth: 2.5,
      isStrokeCapRound: true,
      dotData: FlDotData(
        show: false,
      ),
      belowBarData: BarAreaData(show: false, colors: [
        const Color(0x00aa4cfc),
      ]),
    );
    return [
      lineChartBarData1,
      lineChartBarData2,
    ];
  }

  LineChartData sampleData2() {
    return LineChartData(
      lineTouchData: LineTouchData(
        enabled: false,
      ),
      gridData: FlGridData(
        show: false,
      ),
      titlesData: FlTitlesData(
        bottomTitles: SideTitles(
          showTitles: true,
          reservedSize: 22,
          textStyle: chartLabelsTextStyle,
          margin: 10,
          getTitles: (value) {
            switch (value.toInt()) {
              case 2:
                return 'SEPT';
              case 7:
                return 'OCT';
              case 12:
                return 'DEC';
            }
            return '';
          },
        ),
        leftTitles: SideTitles(
          showTitles: true,
          textStyle: chartLabelsTextStyle,
          getTitles: (value) {
            switch (value.toInt()) {
              case 1:
                return '1m';
              case 2:
                return '2m';
              case 3:
                return '3m';
              case 4:
                return '5m';
              case 5:
                return '6m';
            }
            return '';
          },
          margin: 8,
          reservedSize: 30,
        ),
      ),
      borderData: FlBorderData(
          show: true,
          border: const Border(
            bottom: BorderSide(
              color: Color(0xff4e4965),
              width: 4,
            ),
            left: BorderSide(
              color: Colors.transparent,
            ),
            right: BorderSide(
              color: Colors.transparent,
            ),
            top: BorderSide(
              color: Colors.transparent,
            ),
          )),
      minX: 0,
      maxX: 14,
      maxY: 6,
      minY: 0,
      lineBarsData: linesBarData2(),
    );
  }

  List<LineChartBarData> linesBarData2() {
    return [
      LineChartBarData(
        spots: [
          FlSpot(1, 1),
          FlSpot(3, 4),
          FlSpot(5, 1.8),
